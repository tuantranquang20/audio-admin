const get7Day = () => {
  const today = moment();

  const pastDays = [];

  for (let i = 0; i < 7; i++) {
    pastDays.push(today.clone().subtract(i, "days").format("YYYY-MM-DD"));
  }

  return pastDays.reverse();
};
const getTodayAnd7Day = () => {
  const today = moment();

  return `${today.clone().subtract(7, "days").format("YYYY/MM/DD")} - ${today.clone().format("YYYY/MM/DD")}`;
};

